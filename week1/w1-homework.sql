-- 2019-01-15
SELECT DATE(LPEP_PICKUP_DATETIME),
	MAX(TRIP_DISTANCE)
FROM TRIPS
GROUP BY DATE(LPEP_PICKUP_DATETIME)
ORDER BY MAX DESC 



