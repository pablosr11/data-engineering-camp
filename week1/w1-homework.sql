-- Long Island City/Queens Plaza
SELECT DOZ.ZONE
FROM TRIPS TT
INNER JOIN ZONES PUZ ON TT.PULOCATIONID = PUZ.LOCATIONID
INNER JOIN ZONES DOZ ON TT.DOLOCATIONID = DOZ.LOCATIONID
WHERE PUZ.ZONE = 'Astoria'
ORDER BY TIP_AMOUNT DESC
LIMIT 1